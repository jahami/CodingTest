def 솔루션(tupyoham):
    투표수 = [0, 0, 0]     #[0]:심영 [1]:김두한 [2]:상하이조

    # x는 tupyoham에 있는 모든 표를 순서대로 반복:
    for x in tupyoham:
        #만약 x가 "심영"이라면:
        if x == "심영":
            투표수[0] += 1
        if x == "김두한":
            투표수[1] += 1
        if x == "상하이조":
            투표수[2] += 1
    print(f"심영:{투표수[0]} 김두한:{투표수[1]} 상하이조:{투표수[2]}")
tupyoham = ["심영", "심영", "김두한", "심영", "상하이조", "김두한", "심영"]
솔루션(tupyoham)